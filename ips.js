// ================= LOGIKA KUIS =================
let nomor = 0;
let skor = 0;
let kelasDipilih = "7";

function mulaiKuis() {
    kelasDipilih = document.getElementById("kelas").value;
    nomor = 0;
    skor = 0;

    document.getElementById("quiz").classList.remove("hidden");
    document.getElementById("hasil").classList.add("hidden");

    tampilSoal();
}

function tampilSoal() {
    const data = soal[kelasDipilih][nomor];
    document.getElementById("soalText").innerText =
        "Soal " + (nomor + 1) + ": " + data[0];

    const opsiDiv = document.getElementById("options");
    opsiDiv.innerHTML = "";

    data[1].forEach(pilihan => {
        let btn = document.createElement("div");
        btn.className = "option";
        btn.innerText = pilihan;
        btn.onclick = () => jawab(pilihan);
        opsiDiv.appendChild(btn);
    });
}

function jawab(pilihan) {
    const benar = soal[kelasDipilih][nomor][2];

    if (pilihan === benar) skor += 5;

    nomor++;
    if (nomor >= 20) selesai();
    else tampilSoal();
}

function selesai() {
    document.getElementById("quiz").classList.add("hidden");
    const hasil = document.getElementById("hasil");
    hasil.classList.remove("hidden");
    hasil.innerHTML = "Kuis Selesai!<br>Nilai Kamu: <b>" + skor + "</b>";
}